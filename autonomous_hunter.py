async def run_queue_mode():
    print(f"🦇 Asynchronous Queue Engine Active. Watching '{QUEUE_FILE}'...")
    if not os.path.exists(QUEUE_FILE): open(QUEUE_FILE, 'w').close()

    MAX_RETRIES = 12 
    last_retry_time = 0
    RETRY_INTERVAL = 300 

    # Open persistent asynchronous HTTP connection client session pool
    async with httpx.AsyncClient() as client:
        while True:
            try:
                pending_retries = load_waiting_room()
                target = get_target_from_queue()
                
                if target:
                    if target not in pending_retries:
                        status = await scan_address(client, target)
                        
                        if status == "RATE_LIMIT":
                            print(f"   [WAITING] ⏳ Moving {target} to Waiting Room until API quota resets.")
                            pending_retries[target] = 0
                            save_waiting_room(pending_retries)
                    
                    await asyncio.sleep(1.0)
                else:
                    await asyncio.sleep(10.0)

                current_time = time.time()
                if pending_retries and (current_time - last_retry_time > RETRY_INTERVAL):
                    print(f"\n🔄 Checking {len(pending_retries)} rate-limited targets inside the Waiting Room...")
                    
                    for addr in list(pending_retries.keys()):
                        status = await scan_address(client, addr)
                        
                        if status == "RATE_LIMIT":
                            print(f"   [WAITING] ⏳ API quota limit still active. Retry index: {pending_retries[addr] + 1}/{MAX_RETRIES}")
                            pending_retries[addr] += 1
                            
                            if pending_retries[addr] >= MAX_RETRIES:
                                print(f"   [TIMEOUT] 🗑️ Purging {addr} from state tracker - Exceeded retry limit boundaries.")
                                del pending_retries[addr]
                        else:
                            del pending_retries[addr]
                            
                        await asyncio.sleep(1.0) 
                        
                    save_waiting_room(pending_retries)
                    last_retry_time = time.time()

            except KeyboardInterrupt:
                print("\n👋 Halting Hunter execution loop.")
                break
            except Exception as e:
                print(f"❌ Ingestion loop fault: {e}")
                await asyncio.sleep(10.0)

async def main():
    import argparse
    parser = argparse.ArgumentParser(description="0xNeural V2 Autonomous Hunter")
    parser.add_argument("target", nargs="?", help="Contract address, file path, or 'queue'")
    parser.add_argument("--force", action="store_true", help="Skip triage and scan regardless of logic complexity")
    args = parser.parse_args()

    if not args.target or args.target == "queue":
        await run_queue_mode()
    elif os.path.isfile(args.target):
        with open(args.target, 'r') as f:
            addresses = [line.strip() for line in f if line.strip().startswith('0x')]
        async with httpx.AsyncClient() as client:
            for addr in addresses:
                await scan_address(client, addr, force=args.force)
                await asyncio.sleep(1.0)
    else:
        async with httpx.AsyncClient() as client:
            await scan_address(client, args.target, force=args.force)

if __name__ == "__main__":
    try:
        asyncio.run(main())
    except KeyboardInterrupt:
        print("\n👋 Process aborted cleanly.")