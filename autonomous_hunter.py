# 🔬 STATE-CONSISTENCY & STORAGE-FLOW FILTER
# ==========================================
def verify_state_consistency(source_code, report_text):
    """
    Validates engine-flagged access control and modifier omissions against internal storage slots.
    Bypasses false positives generated by Diamond/AppStorage library internal constraints.
    """
    storage_pointers = ["_getStorage()", "DiamondStorage", "AppStorage", "ds.manager", "position.slot"]
    has_internal_storage_routing = any(p in source_code for p in storage_pointers)
    
    is_modifier_alert = any(term in report_text.lower() for term in [
        "unprotected external function", "missing access control", 
        "missing modifier", "lack of authority modifiers"
    ])
    
    if is_modifier_alert and has_internal_storage_routing:
        internal_auth_checks = [r"msg\.sender\s*==\s*", r"require\s*\(.*manager", r"AuthLibrary\.", r"assert\s*\(.*owner"]
        for pattern in internal_auth_checks:
            if re.search(pattern, source_code):
                print("🛡️ [STATE-CONSISTENCY] Intercepted proxy storage library internal authorization check.")
                return False  
                
    return True  

