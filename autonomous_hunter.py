# 🧠 CORE SCANNING LOGIC (ASYNC ARCHITECTURE)
# ==========================================
async def scan_address(client: httpx.AsyncClient, address: str, force: bool = False) -> str:
    print(f"\n⚡ Ingesting target: {address}")
    
    # --- EDGE INTERCEPT 1: Canonical Token Whitelist Matrix ---
    if is_canonical_address(address):
        print(f"🛡️ [WHITELIST-INTERCEPT] Skipping {address} - Verified immutable asset pool ledger.")
        return "DONE"

    # --- NEW ASYNC OSINT DIRECTIVE: AUTOMATED ECONOMIC TRIAGE ---
    # Instantly discards zero-value configurations before hitting inference models
    if not force and not await check_economic_viability(address, min_eth=0.01):
        return "DONE"

    # --- INGESTION PHASE: Fetch Source Code Payload ---
    # (Kept synchronous as it relies on connection-pooled third-party retrieval)
    source_code = fetch_source_code(address)
    if not source_code:
        print(f"❌ [FETCH-ERROR] Could not isolate source layout payload for {address}. Dropping target.")
        return "DONE" 

    # --- EDGE INTERCEPT 2: AST Gatekeeper Regex Triage Check ---
    if not triage_contract_ast(source_code, force_bypass=force):
        print(f"🗑️ [AST-FILTER-DROP] Dropping {address} - Invariant code clone detected (No loops/callbacks).")
        return "DONE"

    # --- HISTORICAL INVARIANT SCRIPT TRIAGE MATCHING ---
    if not triage_contract(source_code) and not force:
        print(f"⏭️ Skipped {address} - Baseline structural code invariants missing.")
        return "DONE"

    # --- OSINT BOUNCER CHECK WINDOWS ---
    print(f"🕵️ Running OSINT checks before Engine analysis...")
    creator_wallet, tx_hash = get_contract_creator(address)
    
    if str(creator_wallet).lower() in ["none", "unknown", "", "null"]:
        print(f"   [BLOCKED] 🗑️ Dropping {address} (Factory Deployed / Infrastructure).")
        return "DONE"
        
    ens_name = resolve_ens_name(creator_wallet) if creator_wallet else "Unknown"
    github_repo = search_github_for_address(address)
    
    if ("Anonymous Wallet" in ens_name or "Unknown" in ens_name) and ("No public repo found" in github_repo or "Failed" in github_repo):
        print(f"   [BLOCKED] 🗑️ Dropping {address} (No Identity + No Repo = No Bounty).")
        with open(LOGS_DIR / "dropped_scams.txt", "a") as f:
            f.write(f"{time.strftime('%Y-%m-%d %H:%M:%S')} - {address} (Low Signal)\n")
        return "DONE" 

    # --- INFERENCE ENGINE ROUTING LAYER (NON-BLOCKING) ---
    print(f"🎯 [HIGH-SIGNAL] Approved! Passing {address} to 0xNeural V2 Agentic Core...")
    try:
        # Asynchronously transmit contract payload to back-end worker node
        response = await client.post(LOCAL_API_URL, json={"source_code": source_code}, timeout=120.0)
        
        if response.status_code == 200:
            res_json = response.json()
            
            # Catch deterministic structured back-end drop instructions
            if res_json.get("status") == "dropped":
                print(f"   [TRASHED] 📉 Multi-Agent Filter: {res_json.get('reason')}")
                return "DONE"
                
            report = res_json.get('report', '')
            
            if not verify_state_consistency(source_code, report):
                print(f"   [TRASHED] 📉 Invariant Filter Override: Access check resides in library storage frame.")
                with open(LOGS_DIR / "filtered_false_positives.txt", "a") as f:
                    f.write(f"{time.strftime('%Y-%m-%d %H:%M:%S')} - {address} (Internal Proxy Library Access Protection Enforced)\n")
                return "DONE"

            # --- NEW ANVIL SANDBOX PROPERTY-BASED FUZZ FILTER ---
            rpc_url = os.getenv("ALCHEMY_RPC_URL")
            if not rpc_url:
                print("   ⚠️ [WARNING] ALCHEMY_RPC_URL missing from .env. Skipping Anvil Sandbox verification.")
                is_verified = True 
            else:
                print("   🧪 Routing to Anvil Sandbox for automated property fuzzing validation...")
                is_verified = await run_dynamic_verification(report, rpc_url)

            if is_verified:
                print(f"🎯 ZERO-DAY CONFIRMED BY PROPERTY FUZZER! Compiling localized advisory report...")
                filename = REPORTS_DIR / f"VULN_{address}.md"
                osint_data = (
                    f"\n\n## 🕵️ Automated OSINT Dossier\n"
                    f"- **Deployer Wallet:** `{creator_wallet}`\n"
                    f"- **Deployer ENS:** `{ens_name}`\n"
                    f"- **Creation Tx:** `{tx_hash}`\n"
                    f"- **Likely GitHub Repo:** {github_repo}\n"
                )
                
                with open(filename, 'w', encoding='utf-8') as f:
                    f.write(f"# Audit Report: {address}\n**Scan Date:** {time.strftime('%Y-%m-%d %H:%M:%S')}\n**High Value Logic:** True\n\n")
                    clean_report = report.replace("High Value Logic: True\n", "").strip()
                    f.write(clean_report)
                    f.write(osint_data)
                    
                send_discord_alert(address, clean_report, osint_data)
                return "DONE"
            else:
                print("   🗑️ FALSE POSITIVE FILTERED. Sandbox fuzzer failed to break contract properties. Dropping.")
                with open(LOGS_DIR / "filtered_false_positives.txt", "a") as f:
                    f.write(f"{time.strftime('%Y-%m-%d %H:%M:%S')} - {address} (Failed Property Fuzz execution)\n")
                return "DONE"
            
        elif response.status_code == 500:
            print(f"   ⚠️ [API ERROR] Server returned internal fault code 500. Verification limit threshold reached.")
            return "RATE_LIMIT"
        else:
            print(f"❌ Engine error code returned: {response.status_code}")
            return "DONE"
            
    except httpx.ConnectError:
        print("❌ System Error: Cannot resolve backend connection. Verify that app.py uvicorn server is active.")
        return "DONE"

